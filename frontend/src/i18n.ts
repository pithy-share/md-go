type Key = keyof typeof zh;

const dictionaries: Record<Locale, Record<Key, string>> = {
  zh: zh as Record<Key, string>,
  en,
};

function readInitialLocale(): Locale {
  try {
    const saved = localStorage.getItem('md-go-locale');
    if (saved === 'en' || saved === 'zh') return saved;
  } catch { /* localStorage may be unavailable */ }
  return 'zh';
}

// Initialize from the persisted preference at module load so the very first
// render (e.g. the "ready" toast) already reflects the user's chosen language.
let currentLocale: Locale = readInitialLocale();

export function getLocale(): Locale {
  return currentLocale;
}

export function setLocale(locale: Locale): void {
  currentLocale = locale;
}

export function t(key: Key, values: Values = {}): string {
  const dict = dictionaries[currentLocale] ?? dictionaries.zh;
  const template = dict[key] ?? dictionaries.zh[key] ?? key;
  return template.replace(/\{(\w+)}/g, (_, name) => String(values[name] ?? ''));
}
